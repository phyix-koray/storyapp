import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { BrowserRouter, Route, Routes } from "react-router-dom";
import { Toaster as Sonner } from "@/components/ui/sonner";
import { Toaster } from "@/components/ui/toaster";
import { TooltipProvider } from "@/components/ui/tooltip";
import Index from "./pages/Index.tsx";
import ExploreRoute from "./pages/ExploreRoute.tsx";
import CreateRoute from "./pages/CreateRoute.tsx";
import AssetsRoute from "./pages/AssetsRoute.tsx";
import ProfileRoute from "./pages/ProfileRoute.tsx";
import NotFound from "./pages/NotFound.tsx";

const queryClient = new QueryClient();

const App = () => (
  <QueryClientProvider client={queryClient}>
    <TooltipProvider>
      <Toaster />
      <Sonner />
      <BrowserRouter>
        <Routes>
          <Route path="/" element={<Index />} />
          <Route path="/explore" element={<ExploreRoute />} />
          <Route path="/create" element={<CreateRoute />} />
          <Route path="/assets" element={<AssetsRoute />} />
          <Route path="/profile" element={<ProfileRoute />} />
          <Route path="*" element={<NotFound />} />
        </Routes>
      </BrowserRouter>
    </TooltipProvider>
  </QueryClientProvider>
);

export default App;
